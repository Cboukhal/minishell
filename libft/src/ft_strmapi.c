#include "../include/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	len;
	char	*strmapi;

	i = 0;
	if (!s || !(*f))
		return (NULL);
	len = ft_strlen(s);
	strmapi = malloc(sizeof(char) * len + 1);
	if (!strmapi)
		return (NULL);
	while (s[i])
	{
		strmapi[i] = f(i, s[i]);
		i++;
	}
	strmapi[i] = '\0';
	return (strmapi);
}
